void compete_solution(vector<int>&nums)
{
    int n;
    cin >> n;
    //print(nums);
    int left=0,right=nums.size();
    while(left<right){
        int mid=(left+right)/2;
        if(nums[mid]<=n){
            left=mid+1;
        }
        else{
            right=mid;
        }

    }
    cout<<left-1<<endl;
}