class Trie
{
    public:
    struct node
    {
        vector<node*> child;
        bool is_end;
        node()
        {
            vector<node*> temp(10,nullptr);
            child=temp;
            is_end=false;
        }
    };
    node* root;
    Trie()
    {
        root=new node();
    }
    bool insert(string &s)
    {
        node* cur=root;
        for(char &c:s)
        {
            if(cur->child[c-'0']==nullptr)
                cur->child[c-'0']=new node();
            cur=cur->child[c-'0'];
        }
        bool temp=cur->is_end;
        cur->is_end=true;
        return temp;
    }
};
